s=[]
for i in range(20):
    for k in range(34):
        j=100-i-k
        if 5*i+3*k+j/3==100:
            s.append([i,k,j])

print('总共有%d个解'%len(s))
print('解1:鸡翁 ',s[0][0],' 鸡母 ',s[0][1],' 鸡雏 ',s[0][2])
print('解2:鸡翁 ',s[1][0],' 鸡母 ',s[1][1],' 鸡雏 ',s[1][2])
print('解3:鸡翁 ',s[2][0],' 鸡母 ',s[2][1],' 鸡雏 ',s[2][2])
print('解4:鸡翁 ',s[3][0],' 鸡母 ',s[3][1],' 鸡雏 ',s[3][2])
